/**************************************************************************************************************************************************************
1行目で正の整数nが入力され、続くn行の各行で整数が1つずつ入力されます。n個の整数を小さい順に改行区切りで出力してください。

■入力される値

    入力は以下のフォーマットで与えられます。

    n (数字の総数)
    a_1
    a_2
    a_3
    ...
    a_i
    ...
    a_n

    入力値最終行の末尾に改行が１つ入ります。
    文字列は標準入力から渡されます。
***************************************************************************************************************************************************************/

